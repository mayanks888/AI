# Program to draw rectangle in Python Turtle
import turtle

t = turtle.Turtle()
t.forward(150)  # Forward turtle by 150 units
t.left(90)  # Turn turtle by 90 degree
t.forward(80)  # Forward turtle by 80 units
t.left(90)  # Turn turtle by 90 degree
t.forward(150)  # Forward turtle by 150 units
t.left(90)  # Turn turtle by 90 degree
t.forward(80)  # Forward turtle by 80 units
t.left(90)  # Turn turtle by 90 degree
